#Built-ins functions do tipo <list> (muito utilizadas).

len()     #retorna o número elementos de uma lista
sum()     #retorna a sima dos números de uma lista.
any()     #retorna True se qualquer um dos valores booleanos de uma lista for verdadeiro.
all()     #retorna True se TODOS os valores booleanos de uma lista forem verdadeiros.
sorted()  #retorna uma cópia modificada(odernada) de uma lista. mas mantém a lista original intacta (na memória).
min()    #min(lista)  retorna o menor/maior elemento da lista. Valor string, retorna com menor caracter e ordem alfabética.
max()    #min(lista)  retorna o menor/maior elemento da lista. Valor string, retorna com maior número de caracteres e ordem alfabética.
extend()  #acrescenta os elementos ao final da lista.