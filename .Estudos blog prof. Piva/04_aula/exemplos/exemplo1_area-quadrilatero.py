#Calcular a área de um quadrilátero, com dois lados iguais. Para isso faz a leitura de dois numeros int, representando os lados e imrpime os valores lidos e a área calculdada.

altura = float(input("Digite o valor altura: "))
base = float(input("Digite o valor da base: "))
area = base * altura
print(f"A área é: {area}. Altura é: {altura}. A base é: {base}")